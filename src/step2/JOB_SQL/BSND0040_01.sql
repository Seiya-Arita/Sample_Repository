# フェーズ          :テーブル作成
# サイクル          :日次
# 実行日            :DAYAFTER(火曜日～土曜日）
# 参照テーブル      :世帯名寄せ情報			|	T_CM02010D_OBJ
#
# 備考              :
#
# 変更履歴
# -------------------------------------------------------------------------
